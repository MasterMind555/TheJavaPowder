package thejavapowder;

import java.awt.*;

public class ImageTemplate {

	public ImageTemplate(Image image, int x, int y, int width, int height)
	{
		this.image = image;
		this.x = x;
		this.y = y;
		this.Width = width;
		this.Height = height;
	}

	public Image image;

	public int x;
	public int y;

	public int Width;
	public int Height;

}
